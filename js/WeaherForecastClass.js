class WeatherForecast {

    date;

    temperatureC;

    temperatureF;

    summary;

    liveData;

    location;

    constructor(dateInput, temperatureCInput, temperatureFInput, summaryInput, liveDataInput, locationInput) {
        this.date = dateInput;
        this.temperatureC = temperatureCInput;
        this.temperatureF = temperatureFInput;
        this.summary = summaryInput;
        this.liveData = liveDataInput;
        this.location = locationInput;
    }

}






